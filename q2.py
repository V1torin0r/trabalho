# Produto de Dois Números
#  Escreva uma função chamada `produto` que receba dois números inteiros como parâmetros e
# retorne o produto deles.
#  Exemplo de entrada: `produto(4, 7)`
#  Saída esperada: `28`

def produto(n1, n2):
    prod = n1*n2
    return prod


print (produto(2, 5))
    